import json
import re
from pathlib import Path
from typing import List, Set


ROOT = Path(__file__).resolve().parents[1]
JSON_DIR = ROOT / "mavis" / "json"
HANDLERS_DIR = ROOT / "core" / "inst_handlers"


def ext_from_filename(name: str) -> str:
    """
    Extract extension suffix from isa_rv64*.json.
    Example: isa_rv64zicond.json -> zicond
    """
    m = re.match(r"isa_rv64(.*)\.json$", name)
    if not m:
        raise ValueError(f"Unexpected filename format: {name}")
    return m.group(1)


def find_existing_exts() -> Set[str]:
    """
    Find existing Rv*Insts.hpp classes and derive their ext names.
    Example: RvzicondInsts.hpp -> zicond, RviInsts.hpp -> i
    """
    base = Path(HANDLERS_DIR)
    exts: Set[str] = set()
    for path in base.rglob("Rv*Insts.hpp"):
        stem = path.stem  # e.g. RvzicondInsts
        if not (stem.startswith("Rv") and stem.endswith("Insts")):
            continue
        core = stem[2:-5]  # strip leading 'Rv' and trailing 'Insts'
        if core:
            exts.add(core.lower())
    return exts


